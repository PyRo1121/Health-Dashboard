import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const workflowsDir = '.github/workflows';
const outputDir = 'docs/ops';
const outputFile = join(outputDir, 'ci-workflow-inventory.md');

const files = readdirSync(workflowsDir)
  .filter((file) => file.endsWith('.yml') || file.endsWith('.yaml'))
  .sort((a, b) => a.localeCompare(b));

function parseName(content, fallback) {
  const match = content.match(/^name:\s*(.+)$/m);
  return match ? match[1].trim() : fallback;
}

function parseTriggers(content) {
  const onLine = content.match(/^on:[ \t]*(.+)$/m);
  if (onLine && onLine[1] && !onLine[1].includes('{')) {
    return onLine[1].trim();
  }

  const blockMatch = content.match(/^on:\s*\n([\s\S]*?)(?:^\S|\Z)/m);
  if (!blockMatch) return 'manual/other';

  const triggerLines = [...blockMatch[1].matchAll(/^  ([a-zA-Z_]+):/gm)].map((match) => match[1]);

  if (triggerLines.length === 0) return 'manual/other';
  return [...new Set(triggerLines)].join(', ');
}

const rows = files.map((file) => {
  const path = join(workflowsDir, file);
  const content = readFileSync(path, 'utf8');
  const name = parseName(content, file);
  const triggers = parseTriggers(content);
  return `| ${name} | \`${file}\` | ${triggers} |`;
});

const markdown = `# CI Workflow Inventory\n\nThis file is auto-generated by \`scripts/generate-workflow-inventory.mjs\`.\n\n## Workflows\n\n| Workflow | File | Triggers |\n| --- | --- | --- |\n${rows.join('\n')}\n\n## Notes\n\n- Use this as a quick index for audits and branch-protection reviews.\n- Update via the \`Docs Inventory Sync\` workflow or by running:\n\n\`\`\`sh\nnode scripts/generate-workflow-inventory.mjs\n\`\`\`\n`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputFile, markdown);
console.log(`Generated ${outputFile}`);
